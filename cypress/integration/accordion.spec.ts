describe('accordion', () => {
  before(() => {
    cy.visit('/components/accordion')
  })

  it('checks accordion title opening and closing', () => {
    cy
      .get('.ui-accordion__content')
      .eq(0)
      .should('be.visible')
      .get('.ui-accordion__content')
      .eq(1)
      .should('not.be.visible')
      .get('.ui-accordion__title')
      .eq(0)
      .click()
      .get('.ui-accordion__content')
      .eq(0)
      .should('not.be.visible')
      .get('.ui-accordion__title')
      .eq(1)
      .click()
      .get('.ui-accordion__content')
      .eq(1)
      .should('be.visible')
  })

  it('checks the exclusive character of accordion', () => {
    cy
      .get('.ui-accordion__content')
      .eq(2)
      .should('not.be.visible')
      .get('.ui-accordion__title')
      .eq(2)
      .click()
      .get('.ui-accordion__content')
      .eq(2)
      .should('be.visible')
      .get('.ui-accordion__content')
      .eq(3)
      .should('not.be.visible')
      .get('.ui-accordion__content')
      .eq(4)
      .should('not.be.visible')
      .get('.ui-accordion__title')
      .eq(3)
      .click()
      .get('.ui-accordion__content')
      .eq(2)
      .should('not.be.visible')
      .get('.ui-accordion__content')
      .eq(3)
      .should('be.visible')
      .get('.ui-accordion__content')
      .eq(4)
      .should('not.be.visible')
      .get('.ui-accordion__title')
      .eq(4)
      .click()
      .get('.ui-accordion__content')
      .eq(2)
      .should('not.be.visible')
      .get('.ui-accordion__content')
      .eq(3)
      .should('not.be.visible')
      .get('.ui-accordion__content')
      .eq(4)
      .should('be.visible')
  })
})
